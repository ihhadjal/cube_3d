#include "../../resources/cube.h"

int	cal_h(char **map)
{
	int	i;
	int	j;
	int	h;

	h = 0;
	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			if (map[i][j] == '1')
			{
				h++;
				break ;
			}
			j++;
		}
		i++;
	}
	return (h);
}
